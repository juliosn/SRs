    private void atualizarComboBox(){
        String pesquisa = "SELECT * FROM condominio";
        try 
        {
            ps = con.prepareStatement(pesquisa);
            rs = ps.executeQuery();

            while (rs.next()) {
                jcb_condominio.addItem(rs.getString("nome_condominio") + " - " + rs.getString("cnpj"));
            }
        }
        catch (SQLException e) 
        {
            JOptionPane.showMessageDialog(null, "A listagem de dados falhou!", "Mensagem do Programa", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void definirCor(){
        if(tipoTela == "ativos"){
            btn_gerenc_port.setBackground(new Color(0,53,143));
            btn_port_in.setBackground(new Color(51,121,241));
        }
        else{
            btn_port_in.setBackground(new Color(0,53,143));
            btn_gerenc_port.setBackground(new Color(51,121,241));            
        }
    }
    
