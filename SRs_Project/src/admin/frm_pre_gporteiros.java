    private void btn_AvancarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AvancarActionPerformed
        
        String selectedItem = (String) jcb_condominio.getSelectedItem();
        
        if (selectedItem != null) 
        {
        String[] parts = selectedItem.split(" - ");
            if (parts.length == 2)
            {
                String nomeCondominio = parts[0];
                String cnpj = parts[1];
                
                try {
                    String pesquisaIdMorador = "SELECT id_condominio FROM condominio WHERE nome_condominio = ? AND cnpj = ?";
                    ps = con.prepareStatement(pesquisaIdMorador);
                    ps.setString(1, nomeCondominio);
                    ps.setString(2, cnpj);

                    rs = ps.executeQuery();
                    if (rs.next()) {
                        int idCond = rs.getInt("id_condominio");
                        setIdConSelecionado(idCond);
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Não foi possível obter os dados requisitados!", "Mensagem do Programa", JOptionPane.ERROR_MESSAGE);
                }

            } 
            else 
            {
                JOptionPane.showMessageDialog(null, "Não foi possível obter os dados requisitados!", "Mensagem do Programa", JOptionPane.ERROR_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Por favor, selecione um condomínio para acessar o serviço desejado!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
        
        if("ativos".equals(tipoTela)){
            frm_gerenc_porteiros mostra = new frm_gerenc_porteiros(getIdConSelecionado());
            mostra.setVisible(true);
            dispose();
        }
        else{
            frm_port_inativos mostra = new frm_port_inativos(getIdConSelecionado());
            mostra.setVisible(true);
            dispose();
        }
        
    }//GEN-LAST:event_btn_AvancarActionPerformed

